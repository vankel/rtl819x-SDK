#!/bin/sh
aipc_util -C IOCTL_DBG_PRINT -F DBG_NONE
aipc_util -C IOCTL_DBG_PRINT -F DBG_CONT
aipc_util -C IOCTL_DBG_PRINT -F DBG_ALL

aipc_util -C IOCTL_DBG_PRINT -F DBG_CTRL
aipc_util -C IOCTL_DBG_PRINT -F DBG_MBOX
aipc_util -C IOCTL_DBG_PRINT -F DBG_INTQ

aipc_util -C IOCTL_DBG_DUMP  -F DUMP_MBOX
aipc_util -C IOCTL_DBG_DUMP  -F DUMP_CTRL

aipc_util -C IOCTL_IPC_MUTEX_LOCK      -F    PROCESSOR_ID_CPU
aipc_util -C IOCTL_IPC_MUTEX_LOCK      -F    PROCESSOR_ID_DSP

aipc_util -C IOCTL_IPC_MUTEX_TRYLOCK   -F    PROCESSOR_ID_CPU
aipc_util -C IOCTL_IPC_MUTEX_TRYLOCK   -F    PROCESSOR_ID_DSP

aipc_util -C IOCTL_IPC_MUTEX_UNLOCK    -F    PROCESSOR_ID_CPU
aipc_util -C IOCTL_IPC_MUTEX_UNLOCK    -F    PROCESSOR_ID_DSP

aipc_util -C IOCTL_IPC_MUTEX_OWN       -F    PROCESSOR_ID_CPU
aipc_util -C IOCTL_IPC_MUTEX_OWN       -F    PROCESSOR_ID_DSP
